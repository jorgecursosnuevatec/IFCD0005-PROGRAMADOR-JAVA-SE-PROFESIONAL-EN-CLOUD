package principal;

public class UsoOperadores {

	public static void main(String[] args) {
		int a=10, b=7, c=4;
		System.out.println(a/b);
		System.out.println(a==b);
		System.out.println(a%b);
		System.out.println(a&b);
		System.out.println(a+b*c);
		System.out.println(a++);//10
		System.out.println(a);//11
	}

}
